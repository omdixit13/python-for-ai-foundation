# Data Structures for AI Foundation

# List (ordered, mutable)
numbers = [10, 20, 30]
numbers.append(40)
print("List:", numbers)

# Tuple (ordered, immutable)
point = (10, 20)
print("Tuple:", point)

# Set (unique values)
skills = {"python", "ai", "ml", "python"}
print("Set:", skills)

# Dictionary (key-value)
student = {
    "name": "Om",
    "age": 19,
    "branch": "CSE"
}
print("Dictionary:", student)
